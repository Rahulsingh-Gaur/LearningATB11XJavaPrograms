package Self_Assingment;

public class Lab013_Incremnt_Decrement {
    public static void main(String[] args) {
        int z= 10;
        System.out.println(z);
        System.out.println(z++);
        System.out.println(z);
        System.out.println(++z);
        System.out.println(z--);
        System.out.println(z);
        System.out.println(--z);
        System.out.println(z--);
        System.out.println(z);
    }
}


