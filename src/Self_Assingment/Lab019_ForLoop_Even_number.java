package Self_Assingment;
//Write a Java program that prints all the even numbers from 1 to 10.
public class Lab019_ForLoop_Even_number {
    public static void main(String[] args) {

        for (int i=0;i<=10;i++){
            if (i%2==0){
                System.out.println(i);
            }
        }
    }
}
